    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return restaurantNames.count
    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "Cell", for: indexPath) as! RestaurantTableViewCell
        cell.nameLabel.text = restaurantNames[indexPath.row]
        cell.locationLabel.text = restaurantLocations[indexPath.row]
        cell.typeLabel.text = restaurantTypes[indexPath.row]
//        cell.locationLabel.text = restaurantNames[indexPath.row]
        cell.thumbnailImageView.image = UIImage(named:restaurantImages[indexPath.row])
        cell.thumbnailImageView.layer.cornerRadius  = 30.0
        cell.thumbnailImageView.clipsToBounds = true
        
        
        cell.accessoryType = restaurantIsVisited[indexPath.row] ? .checkmark : .none
        
        return cell
    }
    
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        let optionMenu = UIAlertController(title: "bioys", message: "What do you want to do?", preferredStyle: .actionSheet)
        let cancelAction =  UIAlertAction(title: "Cancel", style: .cancel, handler: nil)
        
        let callActionHandler = {(action: UIAlertAction!) -> Void in
            
            let alertMessage = UIAlertController(title: "Service Unavailable", message: "Sorry call feature is not availabl yet. Please retry later.", preferredStyle: .alert)
            alertMessage.addAction(UIAlertAction(title: "OK", style: .default, handler: nil))
            self.present(alertMessage, animated: true, completion: nil)
            
        }
        
        let callAction = UIAlertAction(title: "Call " + "123-456-\(indexPath.row)", style: .default, handler: callActionHandler)
        
        let title = restaurantIsVisited[indexPath.row] ? "Undo Check In" : "Check In"
        let checkInAction =
            UIAlertAction(title: title, style: .default, handler:{ (action: UIAlertAction) -> Void in
                let cell = tableView.cellForRow(at: indexPath)
                cell?.accessoryType = self.restaurantIsVisited[indexPath.row] ? .none : .checkmark
                self.restaurantIsVisited[indexPath.row] = !self.restaurantIsVisited[indexPath.row]
        
            })
        optionMenu.addAction(checkInAction)
        optionMenu.addAction(callAction)
        optionMenu.addAction(cancelAction)
        present(optionMenu, animated: true, completion: nil)
        tableView.deselectRow(at: indexPath, animated: false)
    }

